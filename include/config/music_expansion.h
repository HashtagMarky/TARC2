#define MUSIC_EXPANSION_GEN4_MUSIC_ENABLED              TRUE
#define MUSIC_EXPANSION_GEN5_MUSIC_ENABLED              TRUE
#define MUSIC_EXPANSION_PMDR_MUSIC_ENABLED              TRUE

#endif // GUARD_CONFIG_MUSIC_EXPANSION_H
